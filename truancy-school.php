          <h2>Evasão escolar</h2><br><br>
          <p>
	A pandemia de covid-19 amplificou muitos dos problemas sociais enfrentados no Brasil, e um dos sintomas que atingiram em cheio a educação no país foi o aumento da evasão escolar.<br> 

    <br>Chamamos de evasão escolar quando o estudante, por algum motivo, deixa de ir às aulas, abandonando a escola durante o ano letivo e não retornando para se matricular no ano seguinte.<br>
    <br><h2> Qual é o cenário da evasão escolar no Brasil?</h2><br>
	<br>Antes da pandemia, a evasão escolar já era um problema que afetava uma boa parcela da população brasileira. 
     De acordo com a PNAD Contínua 2019, pesquisa realizada pelo IBGE, 20,2% das pessoas de 14 a 29 anos do país, cerca de 10,1 milhões, não haviam completado alguma das etapas da educação básica.<br> 
     <br>Os dados revelam que o abandono escolar é ainda maior entre pessoas pretas ou pardas (71,7%) e nas regiões Norte e Nordeste do país. 
     A transição do ensino fundamental para o médio acentua ainda mais a evasão escolar. A PNAD 2019 mostrou que, nos alunos de 14 anos, o abandono nas escolas girou em 8,1%, aumentando para 14,1% aos 15 anos.<br>
     <br>Após um ano de restrições causadas pela pandemia, o cenário é ainda mais desolador. Segundo dados da Unicef, 3,8% dos estudantes com idade entre 6 e 17 anos abandonaram as escolas em 2020, aproximadamente 1,38 milhões de pessoas.<br>
     <br>Os dados são maiores do que a média da PNAD do ano anterior, que registrou um percentual de 2% dos estudantes. 
     Além dos estudantes que não frequentavam a escola, 4.125.429 estudantes afirmaram frequentar a escola, mas não tiveram acesso a atividades escolares. A estimativa é que mais de 5,5 milhões de crianças e adolescentes não tiveram acesso à educação em 2020.<br>
     <br><h2>Quais os motivos que levam a evasão escolar? </h2><br>
     <br>Existem muitos fatores de risco que contribuem para o abandono escolar e a consequente evasão nas escolas, ainda mais em um país com altas taxas de desigualdade como o Brasil. Entre os motivos, podemos citar:<br> 

     <br>Dificuldade de acesso à escola;<br>

     Falta de supervisão dos pais;<br> 

     Desinteresse nas aulas;<br> 

     Qualidade da escola;<br> 

     Repetência;<br> 

     Dificuldades de aprendizagem;<br> 

     Problemas financeiros na família.<br> 

     <br>O cenário encontrado durante a pandemia, porém, trouxe outro agravante que aumentou a evasão nas escolas: o acesso à internet e a inclusão digital.<br> 

     <br>Segundo dados da última PNAD Contínua TIC, 17,3% dos domicílios nacionais não possuíam acesso à internet em 2019, cerca de 36,5 milhões de pessoas. 
     Considerando que o ensino remoto foi adotado pela maioria das escolas, podemos ver outro gargalo. A PNAD mostrou que 39% dos estudantes de escolas públicas urbanas afirmaram não ter computador em casa, e 21% têm acesso à internet apenas pelo celular.<br>
     <br>Da falta de equipamentos ao despreparo para o uso de ferramentas virtuais, a comunidade escolar empenhou grandes esforços para garantir a realização de atividades escolares e diminuir o déficit de aprendizagem.<br> 
     <br>No entanto, a dificuldade de acessar o conteúdo das aulas e a necessidade de contornar as dificuldades financeiras impostas pela crise da pandemia levaram muitos alunos a desistirem das aulas em 2020.<br>
    <br><h2>Como evitar a evasão escolar causada pela pandemia? </h2><br>
    <br>Durante a pandemia de covid-19, muitos estudantes tiveram o direito à educação negado, seja por falta de condições de acessar o conteúdo no ensino remoto, seja pela necessidade de distanciamento social.<br> 
    <br>Também vale refletir sobre o perfil dos estudantes que foram mais afetados. Enquanto as escolas privadas e famílias mais ricas conseguiram oferecer condições para que os alunos continuassem estudando, o cenário nas escolas públicas é bem diferente.<br> 
    <br>Por isso, pensar no combate à evasão escolar está diretamente ligado à aplicação de políticas públicas educacionais eficientes, que promovam condições de acesso às tecnologias educacionais e a internet, além de medidas de segurança que garantam a saúde de alunos e professores.<br>
    <br>Veja abaixo algumas medidas que ajudam a evitar a evasão escolar: <br>
    <br>Investir em intervenções pedagógicas<br> 
    Promover o protagonismo do aluno <br>
    Capacitar professores para a educação digital <br>
    Investir em tutoria online <br>

    <br><h2>Como essas medidas se aplica? </h2><br>
    <br>As intervenções pedagógicas são formas de evitar que as dificuldades de aprendizagem virem problemas maiores para os estudantes no futuro. 
    Elas acontecem por meio de estratégias e abordagens educacionais que ajudam o aluno a absorver o aprendizado de forma diferenciada. A intervenção pedagógica traz benefícios como: <br>
    <br>Aumenta o engajamento nas aulas; <br>
    Aproxima a comunidade escolar; <br>
    Melhora o desempenho do estudante; <br>
    Reduz desníveis de aprendizagem. <br>

    <br><h2>Capacitação dos educadores</h2> <br>

    <br>A capacitação dos professores para educação digital é essencial e podemos ver a necessidade dela na pandemia. Pois muitos educadores sentiram ou ainda sentem dificuldades em mexer com certas ferramentas digitais. <br>

    <br><h2>Uso da tecnologia </h2><br>

    <br>A tutoria online é uma atividade de reforço escolar que utiliza meios digitais, como celular ou computador, para colocar o estudante em contato com tutores especialistas.<br> 

    <br>O objetivo da tutoria online é promover um atendimento personalizado e rápido, no qual o estudante pode solucionar suas dúvidas na hora que elas aparecem. Tomando protagonismo da aprendizagem com ajuda da escola.<br>

 

    <br><h2> Sistemas embarcados e IoT</h2><br>

     <br>O uso dos sistemas embarcados na educação traz inúmeros benefícios para os alunos, pois eles incentivam o raciocínio logico, cooperação, trabalho em equipe, solução de problemas e inúmeros outros benefícios. <br>

     <br>É possível aplicar essa tecnologia por meio de palestras, para instigar a vontade dos alunos sobre o tema e por meio de competições de robótica (seja para solucionar algum problema com a tecnologia ou batalhas de robôs) que torna o estudante o protagonista do ensino, onde ele aprende na prática as habilidades técnicas e emocionais de lidar com projetos para o fim de uma solução. <br></p>
