          <h2>Sistemas embarcados na educação</h2><br><br>
          <p>
	Antes de falarmos diretamente dos sistemas embarcados na educação, devemos entender a tecnologia em geral e como ela impacta de forma positiva as vidas dos alunos, professores e da instituição ao todo.<br>   

    <br>A tecnologia é um importante aliado da educação. Além de expor os alunos a novas ferramentas cada vez mais necessárias no cotidiano pessoal e profissional, também oferece recursos para um processo de ensino mais flexível e dinâmico.<br> 

    <br>Especialmente durante esta pandemia, os recursos técnicos são essenciais para que as instituições de ensino continuem a frequentar as aulas remotamente e ficar com os alunos (mesmo que sejam remotos). Mais importante ainda, eles provaram ser uma parte fundamental dos métodos de ensino bem-sucedidos.<br>

    <br> Veja as diversas formas que a tecnologia impacta a educação: 
    <br> Facilita o acesso a materiais e conteúdos complementares 
    <br> Possibilita o uso de recursos multimídia 
    <br> Oferece aos alunos acesso a ferramentas cada vez mais essenciais 
    <br> Contribui positivamente para o desenvolvimento de novas metodologias<br>

	<br><h2>Quais são os diferentes tipos de TICs usados na educação? </h2><br>
	<br>Existem diversos recursos tecnológicos que podem auxiliar no processo de ensino-aprendizagem. De maneira geral, eles podem ser divididos em sete categorias:<br>

    <br>Ambientes virtuais imersivos <br> 
    Ferramentas de comunicação <br> 
    Ferramentas de trabalho <br> 
    Plataformas<br>  
    Principalmente ferramentas experimentais, por meio das quais as instituições conseguem atrair a atenção dos alunos e gerar muito conhecimento prático.<br>  
    <br>Essa tecnologia para a educação coloca o aluno como protagonista do processo de aprendizagem, possibilitando que ele desenvolva projetos e produtos diferenciados. Kits de robôs, plataformas de programação e produção audiovisual (podcasts, vídeos, filmes, música) são apenas alguns exemplos de ferramentas experimentais disponíveis nas escolas.<br>
    
    <br>Além de estudar as habilidades cognitivas, esses recursos estimulam o desenvolvimento da emoção social e das habilidades de comunicação com foco no trabalho em equipe.<br>
    
    <br>E é nesse ponto que os Sistemas Embarcados entram na educação, eles possuem o poder de criar grandes eventos tanto online como físicos, pois é possível criar protótipos com ferramentas de simulação como o Tinkercad. Onde o aluno pode aprender todos os conceitos físicos e lógicos de como desenvolver automações e projetos envolvendo os sistemas embarcados.<br>

    <br><h2>Exemplos reais do uso dessas tecnologias na educação </h2><br>

    <br>Os melhores exemplos que podemos citar sobre como a tecnologia, principalmente os sistemas embarcados, afetam a educação são: os projetos Centro Paula Souza, Etecs e Robótica Paula Souza (do CPS). Essas instituições e projetos buscam oferecer educação técnica gratuita e de alta qualidade e incentivar fortemente o uso da tecnologia.<br>

    <br><h2>O projeto Robótica Paula Souza.</h2><br>

    <br>Podemos ver que esse projeto visa não só as habilidades técnicas, mas também competências éticas e profissionais que com toda certeza impactam positivamente a vida dos estudantes e futuros profissionais. O projeto estimula a curiosidade e conhecimentos tecnológicos de todo tipo, desde robótica, programação, hackathons, Arduino, desafios e muito conhecimento.<br>

   <br>Além de tudo isso já citado eles ainda atuam em olímpiadas, eventos e feiras de tecnologia, ciência e engenharia.<br>

    <br>Podemos ver que o impacto dessas instituições na vida dos alunos é muito grande e todas as outras deveriam seguir o mesmo exemplo, pois a tecnologia não é o futuro, mas sim o presente. E ela vai continuar impactando as nossas vidas e gerando grandes transformações profissionais, educacionais e no nosso dia a dia.<br></p>

