          <h2 class="title">O que são sistemas embarcados?</h2>
          <p class="subtitle">
    Também chamado de sistema embutido, é um sistema microprocessado em que um computador está anexado ao sistema que ele controla. Um sistema embarcado pode realizar um conjunto de tarefas que foram predefinidas. O sistema é usado para tarefas específicas, e assim, através de engenharia é possível otimizar um determinado produto e diminuir o tamanho, bem como os recursos computacionais e o seu valor final. <br>

    <br>Estes podem ser definidos como dispositivos que funcionam como computadores, que contam com memória, processador, interface de entrada e saída, porém, com o diferencial que desempenham uma tarefa específica. Por essa razão, usamos o micro-ondas como exemplo. No geral, os sistemas são usados em aplicações sem muitas exigências. Os sistemas embarcados costumam ser projetados a partir de uma aplicação. O controle remoto é outro bom exemplo, dentro dele há processador, memória, software, enfim, todos os itens encontrados em um computador convencional. Sistemas embarcados costumam ser desenvolvidos para uma tarefa específica. O sistema pode ainda ser executado com recursos computacionais bastante limitados, ou seja, com a ausência de teclado, tela e mesmo com pouca memória.,<br> 

    <br><h2>Processador</h2><br>

    <br>Diferentemente dos computadores que conhecemos, os sistemas embarcados possuem uma capacidade de processamento inferior a eles. Ao invés de usar microprocessadores, os desenvolvedores optam pelo uso de microcontroladores, já eles contam com vários periféricos integrados no mesmo chip. <br>

    <br><h2>Aplicação</h2><br>

    <br> Os sistemas embarcados estão por toda a nossa volta, e por essa razão, não nos damos conta de sua capacidade computacional, já que estamos tão envolvidos com tais mecanismos. Sistemas embarcados operam em máquinas que podem trabalhar por vários anos sem parar, e que ainda, em alguns casos, possuem a capacidade de autocorreção. <br>

   <br> Um excelente exemplo de itens que usam sistemas embarcados são os famosos smartphones, que desempenham funções específicas, e que contam com mecanismos mais limitados que os computadores.<br> 

   <br><h2>O que são microcontroladores?</h2><br>

   <br>Microcontroladores são usados em produtos e equipamentos de automação, como sistemas de controle automotivos, dispositivos médicos implantáveis, controles remotos, máquinas de escritório, eletrodomésticos, ferramentas elétricas, brinquedos e outros sistemas embarcados.<br>
   
   <br>Microcontrolador é um pequeno computador (SoC) num único circuito integrado o qual contém um núcleo de processador, memória e periféricos programáveis de entrada e saída. Os microcontroladores são concebidos para aplicações embarcadas, em contraste com os microprocessadores utilizados em computadores pessoais ou outras aplicações de uso geral.<br> 

   <br>Comparados com projetos que usam dispositivos baseados em microprocessador, os microcontroladores se tornam econômicos para dispositivos e processos de controle digital, reduzindo o tamanho e o custo. Microcontroladores de sinais mistos são comuns e integram os componentes analógicos necessários para controlar sistemas eletrônicos não digitais.<br>
   
   <br>Seu consumo de energia é relativamente baixo, geralmente em miliwatts, e pode entrar em modo de espera (hibernar ou aguardar) para aguardar interrupções ou eventos externos, como pressionar uma tecla ou chegar um sinal pela interface de dados. O consumo de energia desses microcontroladores em modo de espera (standby) pode atingir o nível de nanowatt, por isso são muito adequados para aplicações onde os requisitos de baixo consumo de energia são um fator decisivo para o sucesso do projeto.<br> 

    <br>De forma diferente da programação para microprocessadores, que em geral contam com um sistema operacional e um BIOS, o programador ou projetista que desenvolve sistemas com microcontroladores, geralmente, cria todo programa que será executado pelo sistema ou pode usar um sistema operacional próprio para microcontroladores chamado de RTOS.<br></p>

