          <h2> O que é IoT? </h2><br><br>
          <p>

    <br>A Internet das Coisas (IoT) descreve a rede de — “objetos físicos” — incorporados a sensores, software e outras tecnologias com o objetivo de conectar e trocar dados com outros dispositivos e sistemas pela internet. Esses dispositivos variam de objetos domésticos comuns a ferramentas industriais sofisticadas. Com mais de 7 bilhões de dispositivos IoT conectados hoje, os especialistas esperam que esse número cresça para 10 bilhões em 2020 e 22 bilhões em 2025. <br>

    <br><h2>Por que a Internet das Coisas (IoT) é tão importante? </h2><br>

    <br>Nos últimos anos, a IoT se tornou uma das tecnologias mais importantes do século XXI. Agora que podemos conectar objetos do cotidiano - eletrodomésticos, carros, termostatos, babás eletrônicas - à Internet por meio de dispositivos incorporados, é possível uma comunicação perfeita entre pessoas, processos e outras coisas.<br>

    <br>Por meio da computação de baixo custo, nuvem, big data, análise avançada e tecnologias móveis, coisas físicas podem compartilhar e coletar dados com o mínimo de intervenção humana. Nesse mundo hiperconectado, os sistemas digitais podem gravar, monitorar e ajustar cada interação entre itens conectados. O mundo físico encontra o mundo digital - e eles cooperam. <br>

    <br><h2>Quais Tecnologias Tornaram a IoT Possível? </h2><br>

    <br>Embora a ideia de IoT já exista há muito tempo, uma coleção de avanços recentes em diversas tecnologias diferentes tornou-a prática. <br> 

    <br>Acesso à tecnologia de sensores de baixo custo e baixa potência. Sensores acessíveis e confiáveis estão possibilitando a tecnologia IoT para mais fabricantes. <br> 

    <br>Conectividade. Uma série de protocolos de rede para a Internet facilitou a conexão de sensores à nuvem e a outras “coisas” para transferência eficiente de dados. <br> 

    <br>Plataformas de computação em nuvem. O aumento da disponibilidade de plataformas em nuvem permite que empresas e consumidores acessem a infraestrutura de que precisam para aumentar a escala sem precisar gerenciar tudo. <br> 

    <br>Machine learning e análise avançada. Com os avanços em machine learning e análise avançada, além do acesso a quantidades grandes e variadas de dados armazenados na nuvem, as empresas podem obter insights de maneira mais rápida e fácil. O surgimento dessas tecnologias aliadas continua a ultrapassar os limites da IoT e os dados produzidos pela IoT também alimentam essas tecnologias. <br> 

    <br>Inteligência artificial (IA) conversacional. Os avanços nas redes neurais trouxeram o NLP (natural-language processing, processamento de linguagem natural) aos dispositivos de IoT (como assistentes pessoais digitais Alexa, Cortana e Siri) e os tornaram atraentes, acessíveis e viáveis para uso doméstico.<br>

    <br><h2>O que é IoT Industrial?</h2><br>

    <br>IoT industrial (IIoT) refere-se à aplicação da tecnologia IoT em ambientes industriais, especialmente no que diz respeito à instrumentação e controle de sensores e dispositivos que envolvem tecnologias de nuvem. Consulte oCaso de uso do Titan (PDF) para ter um bom exemplo de IIoT. Recentemente, as indústrias usaram a comunicação máquina a máquina (M2M) para obter automação e controle sem fio. Mas com o surgimento da nuvem e de tecnologias aliadas (como análise avançada e machine learning), os setores podem alcançar uma nova camada de automação e, com ela, criar receitas e modelos de negócios. A IIoT às vezes é chamada de quarta onda da revolução industrial, ou Industry 4.0. A seguir, alguns usos comuns para IIoT:<br><br> 

    Fabricação inteligente <br> 

    Ativos conectados e manutenção preventiva e preditiva <br> 

    Redes de energia inteligentes <br> 

    Cidades inteligentes <br> 

    Logística conectada <br> 

    Cadeias de suprimentos digitais inteligentes<br>


    <br><h2>Quais São Algumas Maneiras pelas quais os Aplicativos IoT são implementados? </h2><br>

    <br>A capacidade da IoT de fornecer informações sobre o sensor e ativar a comunicação dispositivo a dispositivo está impulsionando um amplo conjunto de aplicativos. A seguir, estão alguns dos aplicativos mais populares e o que eles fazem.<br> 

    <br>Crie eficiências em manufatura por meio do monitoramento da máquina e do monitoramento da qualidade do produto. As máquinas podem ser monitoradas e analisadas continuamente para garantir o desempenho dentro das tolerâncias exigidas. Os produtos também podem ser monitorados em tempo real para identificar e solucionar defeitos de qualidade.<br>  

    <br>Melhore o rastreamento e “ring-fence” de ativos físicos. O rastreamento permite que as empresas determinem rapidamente a localização dos ativos. O ring-fence permite que eles garantam que ativos de alto valor estejam protegidos contra roubo e remoção.<br>  

    <br>Use dispositivos vestíveis para monitorar as análises avançadas da saúde humana e as condições ambientais. Os dispositivos vestíveis da IoT permitem que as pessoas entendam melhor sua própria saúde e permitem que os médicos monitorem remotamente os pacientes. Essa tecnologia também permite que as empresas rastreiem a saúde e a segurança de seus funcionários, o que é especialmente útil para trabalhadores empregados em condições perigosas.<br>  

    <br>Impulsione eficiências e novas possibilidades nos processos existentes. Um exemplo disso é o uso de IoT para aumentar a eficiência e segurança em logística conectada para gerenciamento de frotas. As empresas podem usar o monitoramento da frota da Internet das Coisas para direcionar caminhões, em tempo real, para melhorar a eficiência.<br>  

    <br>Habilitar alterações de processos de negócios. Um exemplo disso é o uso de dispositivos IoT para ativos conectados para monitorar a saúde de máquinas remotas e acionar chamadas de serviço para manutenção preventiva. A capacidade de monitorar remotamente as máquinas também está habilitando novos modelos de negócio de produto como serviço, onde os clientes não precisam mais comprar um produto, mas pagam pelo uso.<br>

    <br><h2> Quais Setores Podem se Beneficiar da IoT? </h2><br>

    <br>As organizações mais adequadas para a IoT são aquelas que se beneficiariam do uso de dispositivos sensores em seus processos de negócios.<br> 

    <br><h2> Manufatura</h2><br> 

    <br> Fabricantes podem obter uma vantagem competitiva usando o monitoramento da linha de produção para permitir a manutenção proativa do equipamento quando os sensores detectam uma falha iminente. Os sensores podem realmente medir quando a produção está comprometida. Com a ajuda de alertas de sensor, os fabricantes podem verificar rapidamente a precisão do equipamento ou removê-lo da produção até que seja reparado. Isso permite que as empresas reduzam os custos operacionais, melhorem o tempo de atividade e melhorem o gerenciamento do desempenho dos ativos. <br> 

    <br><h2>Automotivo </h2><br> 

   <br>O setor automotivo espera obter vantagens significativas com o uso de aplicativos de IoT. Além dos benefícios da aplicação da IoT nas linhas de produção, os sensores podem detectar falhas iminentes dos equipamentos nos veículos que já estão na estrada e podem alertar o motorista com detalhes e recomendações. Graças às informações agregadas coletadas pelos aplicativos baseados na IoT, fabricantes e fornecedores automotivos podem aprender mais sobre como manter os carros em movimento e os proprietários de carros informados.<br>  

    <br><h2> Transporte e Logística </h2><br> 

    <br> Os sistemas de transporte e logística se beneficiam de uma variedade de aplicativos de IoT. Frotas de carros, caminhões, navios e trens que possuem estoque podem ser redirecionadas com base nas condições climáticas, disponibilidade de veículo ou motorista, graças aos dados do sensor de IoT. O inventário em si também pode ser equipado com sensores para monitoramento de acompanhamento e rastreamento e controle de temperatura. Os setores de alimentos e bebidas, flores e produtos farmacêuticos costumam realizar inventários sensíveis à temperatura que se beneficiariam muito com os aplicativos de monitoramento da IoT que enviam alertas quando as temperaturas aumentam ou caem para um nível que ameaça o produto. <br> 

     <br><h2> Varejo </h2><br> 

    <br>  Os aplicativos de IoT permitem que as empresas de varejo gerenciem estoques, melhorem a experiência do cliente, otimizem a cadeia de suprimentos e reduzam os custos operacionais. Por exemplo, prateleiras inteligentes equipadas com sensores de peso podem coletar informações baseadas em RFID e enviar os dados para a plataforma IoT para monitorar automaticamente o inventário e acionar alertas se os itens estiverem com pouca carga. Os beacons podem enviar ofertas e promoções direcionadas aos clientes para fornecer uma experiência envolvente. <br> 

     <br><h2> Setor Público</h2><br> 

     <br> Os benefícios da IoT no setor público e em outros ambientes relacionados a serviços são igualmente abrangentes. Por exemplo, os serviços públicos pertencentes ao governo podem usar aplicativos baseados na IoT para notificar seus usuários sobre interrupções em massa e até mesmo sobre interrupções menores nos serviços de água, energia ou esgoto. Os aplicativos de IoT podem coletar dados sobre o escopo de uma interrupção e implementar recursos para ajudar os utilitários a se recuperarem de interrupções com maior velocidade. <br> 

     <br><h2> Assistência Médica</h2><br> 

     <br> O monitoramento de ativos da IoT oferece vários benefícios ao setor de saúde. Médicos, enfermeiros e assistentes geralmente precisam saber a localização exata dos ativos de assistência ao paciente, como cadeiras de rodas. Quando as cadeiras de rodas de um hospital são equipadas com sensores IoT, elas podem ser rastreadas a partir do aplicativo de monitoramento de ativos IoT para que qualquer pessoa que esteja procurando por uma possa encontrar rapidamente a cadeira de rodas disponível mais próxima. Muitos ativos hospitalares podem ser rastreados dessa maneira para garantir o uso adequado e a contabilidade financeira dos ativos físicos em cada departamento. <br> 

     <br><h2> Segurança Geral em Todos os Setores</h2><br>  

     <br> Além de rastrear ativos físicos, a IoT pode ser usada para melhorar a segurança do trabalhador. Os funcionários em ambientes perigosos, como minas, campos de petróleo e gás e usinas químicas e de energia, por exemplo, precisam saber sobre a ocorrência de um evento perigoso que possa afetá-los. Quando estão conectados a aplicativos baseados em sensores da IoT, eles podem ser notificados de acidentes ou resgatados deles o mais rápido possível. Os aplicativos de IoT também são usados para dispositivos vestíveis que podem monitorar a saúde humana e as condições ambientais. Esses tipos de aplicativos não apenas ajudam as pessoas a entender melhor sua própria saúde, como também permitem que os médicos monitorem os pacientes remotamente.<br></p>
