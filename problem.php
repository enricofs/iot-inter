         <h2>Agora vamos para os problemas na educação brasileiras</h2>

     
           <h3><br>Principais problemas da educação no Brasil:<br></h3><br>

        <p>
          • Muitas faculdades e universidades não preparam o professor para a realidade da sala de aula, causado um desânimo na sua profissão<br>
          • Baixa remuneração paga aos professores de Ensino Básico, principalmente da educação pública. Falta de um sistema que beneficie os profissionais mais eficientes<br><br>
          • Carência em sistemas eficientes de aperfeiçoamento, capacitação e educação continuada para professores<br>
          • Currículo pouco interessante para os alunos ou desconectados da realidade<br>

             &emsp; &emsp; • Baixa participação dos pais na vida escolar dos filhos e nos assuntos da escola<br>
             &emsp; &emsp; • Burocracia em excesso na administração escolar<br><br>
          • Investimentos públicos insuficientes para atender com qualidades as necessidades educacionais<br>
          • Elevados índices de repetência, principalmente em regiões mais carentes<br>
             &emsp; &emsp; • Baixa permanência dos alunos nas escolas (média de 4 horas diárias)<br>
             &emsp; &emsp; • Existência de professores lecionando sem formação específica para a área (principalmente em regiões mais carentes do &emsp;&emsp;&emsp;Brasil)<br><br>
          • Uso em excesso de métodos de ensino ultrapassados (questionários, cópias de lição na lousa, muitas aulas teóricas sem participação dos alunos, etc.);<br>
             &emsp; &emsp; • Falta de conexão entre os níveis de ensino (infantil, fundamental e médio); <br><br>
          • Altas taxas de abandono de alunos devido ao fracasso escolar ou problemas financeiros;<br>
          • Carência de condições materiais em escolas de regiões pobres.<br>
          </p>
