          <h2>A tecnologia IoT nas instituições de ensino</h2><br><br>
          <p>
            A UNESCO(Organização das Nações Unidas para a Educação, a Ciência 
            a Cultura) destaca, entre outros benefícios, que as Tecnologias
            contribuem para “a qualidade de ensino e aprendizagem, o
            desenvolvimento profissional de professores, bem como podem melhorar
            a gestão, a governança e a administração educacional ao fornecer a
            mistura certa e organizada de políticas, tecnologias e capacidades”.
            E uma das tecnologias que se destacar nos benefícios que pode trazem
            para a educação e ioT ao utilizar a Internet das Coisas. <br>
    

            E uma das tecnologias que se destacar nos benefícios que pode trazem para a educação e ioT ao utilizar a Internet das Coisas, a escola pode se tornar inteiramente conectada e fornecer informações s a experiência dos alunos em tempo real. Com isso, cria-se um gigantesco banco de dados que possibilita as melhores tomadas de decisão, tanto do ponto de vista de um processo de ensino-aprendizagem mais eficiente quanto administrativo. <br><br>

            Exemplos de coisas que iot poderia facilitar:  <br><br>

            verificação da frequência do aluno; <br>

            localização de funcionários; <br>

           segurança das instalações; <br>

            rastreio de objetos e equipamentos como livros; <br>

            limpeza e organização do patrimônio. <br><br><br>

            <h2> Processo pedagógico e IoT </h2><br>

            Por meio da tecnologia IoT, pode-se monitorar o grau de desempenho e engajamento dos alunos em cada disciplina e assunto específico. Assim, é possível proporcionar mecanismos de aprendizado personalizado, de acordo com as características pessoais de cada estudante, dando um perfil diferenciado às aulas em cada turma. A partir disso, vê-se na escola uma imersão sem precedentes que torna o ambiente interativo, de acordo com as necessidades do aprendizado na era digital. <br><br><br>

            <h2> Quais são as principais características da IoT? </h2><br>

           <h3> Sensores</h3> <br>

           Os equipamentos dotados da tecnologia IoT apresentam sensores. Eles são responsáveis por interpretar os dados e enviá-los a um dispositivo central que armazenará as informações. São sensores de movimento, leitores de chips e de temperatura etc. <br><br>

           <h3>Conectividade</h3> <br>

           A conexão com a Internet se dá no próprio item, em um dispositivo ou por meio de uma estação fixa. Caso esta última seja a opção, ela coletará dados dos objetos com sensores, realizando trocas de mensagens e arquivos. <br><br>

           <h3>Processamento</h3> <br>

           Para que o conceito de Internet das Coisas se concretize, é necessário que essas informações sejam transmitidas a um sistema de controle que faça seu processamento e transforme-as em dados. Quando são utilizados serviços de nuvem, essas informações podem ser transmitidas para o usuário, independentemente de sua geolocalização. <br><br>

            <h3>Aplicações</h3> <br>

           Os dados gerados pela tecnologia IoT permitem que atividades decorrentes do uso daquele equipamento aconteçam de maneira mais rápida, por meio do próprio equipamento. Pode ser um alerta de segurança, uma solicitação de compra de insumos ou um aviso a um professor s seu aluno. <br>

           Além disso, quando associada ao ensino, a IoT oferece múltiplas ferramentas que permitem a criação de planos de aula inteligentes, controle das atividades, verificação do desempenho e engajamento dos alunos, entre outros aspectos pedagógicos difíceis de mensurar sem a tecnologia. <br><br><br>

           <h2> Quais são as principais funções da IoT na escola? </h2><br>

           <h3>Aprendizagem interativa</h3><br>

           No momento em que a educação hda amplia seu espaço e as plataformas de e-learning posicionam-se como um importante canal de aprendizagem, a tecnologia IoT favorece o protagonismo do aluno e permite melhor compreensão da relação entre os conteúdos aprendidos com o contexto do mundo real. Com essa premissa, o professor deixa de ser o detentor do conhecimento para assumir o papel de mediador, estimulando que os alunos encontrem respostas para os desafios inerentes aos temas trabalhados e construam o pensamento crítico.<br><br>

           <h3>Apps educacionais</h3><br>

            A IoT é uma ferramenta poderosa quando utilizada em apps educacionais. Ela transforma o processo de ensino-aprendizagem, pois permite que a escola utilize as formas mais criativas para fomentar a interação dos alunos com os conteúdos, especialmente a distância. <br>

             Por meio desses aplicativos, a escola pode oferecer inúmeros conteúdos interativos, como vídeos e animações, com design responsivo a smartphones, tablets e PCs, o que diversifica as possibilidades de aprendizado. Além disso, estratégias como a gamificação podem ser amplamente utilizadas, estimulando que os alunos cumpram desafios com criatividade. <br>

             Com o avanço da modalidade de Educação a Distância, algumas escolas estão criando aplicativos próprios que permitem uma comunicação integrada entre instituição, pais e alunos. Por meio dessas ferramentas, é possível estudar e realizar tarefas, baixar provas e trabalhos, interagir com colegas e professores, receber feedbacks e ter acesso a notas, presenças, pendências financeiras, entre outras facilidades. <br><br>

             <h3>Acessibilidade</h3><br>

              A tecnologia IoT também contribui para que alunos portadores de necessidades especiais tenham acesso ao ensino facilitado e sintam-se mais participativos e motivados no ambiente escolar.<br> 

              Especialmente porque muitas escolas ainda veem como um grande desafio a inclusão do aluno com deficiência, dispositivos IoT e aplicações são úteis para a personalização das atividades para contemplar esse grupo de estudantes.<br><br>

     
             <h3>Segurança </h3><br>

             No caso das escolas de educação básica, monitorar a localização dos estudantes e saber se estão cumprindo as atividades gatórias do currículo é outro dos grandes desafios, studo quando as instalações são amplas. <br>

             A antiga lista de chamada é substituída, por exemplo, por câmeras com sensor de identificação facial ou por equipamentos que reconhecem os dispositivos dos alunos conectados à rede Wi-Fi da escola. Assim, é possível gerir o controle de presença de maneira mais eficiente. <br>

              E para observar o comportamento dos alunos, existem tecnologias de monitoramento, como a visão inteligente das câmeras, que detectam movimentos suspeitos ou diferentes do normal. <br>

             A visão computacional pode evitar que incidentes inesperados ocorram, inclusive disparando alertas para os coordenadores quando algum aluno estiver circulando em lugar inapropriado ou no horário em que deveria estar na sala de aula. <br>

              Além disso, os riscos de insegurança são grandes, especialmente nos trajetos entre a casa e a escola, quando esses alunos se deslocam desacompanhados. Por isso, a tecnologia IoT fornece opções inteligentes de segurança que possibilitam aos pais saber a exata geolocalização de seus filhos antes e depois do período de aula. <br>

             Alguns dispositivos permitem, inclusive, que os alunos sejam monitorados 24 horas e, se necessário, eles próprios podem acionar botões de socorro. <br><br>

              <h3>Controle de insumos</h3><br>

             Saber quanto a escola gasta com insumos é importante para o controle financeiro. Com a tecnologia IoT, é possível fazer um registro criterioso de praticamente tudo o que é consumido na organização e fazer previsões de custos, a fim de estabelecer tetos de consumo e evitar desperdícios. <br><br><br>
             
            <h2>Quais são as propostas futuras da IoT para a educação? </h2><br>
              A introdução das tecnologias na sala de aula já está mudando os rumos da educação. Embora ainda observemos uma forte influência do modelo tradicionalista de ensino, com carteirasenfileiradas, avaliações classificatórias e transmissão do conhecimento de forma unilateral (do professor para o aluno), a grande tendência é que em um futurove esse cenário se altere significativamente. <br><br>
 
              Graças à transformação digital na educação, com forte presença da tecnologia IoT, a sala de aula deverá se tornar um ambiente de cocriação que alterará positivamente a experiência de aprendizagem dos estudantes, não importa a idade. <br>

             Com a adoção dessas ferramentas digitais, os estudantes ganharão em criatividade e poderão se tornar grandes pesquisadores, com  autossuficiência e amplo envolvimento no projeto pedagógico. Consequentemente, o ensino ficará mais dinâmico e a escola deverá repensar sua infraestrutura para abarcar o novo papel dos educadores. <br>

              Além disso, as parcerias com departamentos ou empresas de serviços de TI aumentarão significativamente para administrar os dados gerados pela tecnologia IoT, a fim de encontrar soluções rápidas para as demandas do dia a dia. <br>

             Além disso, o movimento da cultura maker (ou “faça você mesmo”), em que indivíduos comuns ou profissionais inventam soluções para melhorar seu dia a dia e o de outras pessoas, deverá invadir as escolas. <br>

             Os professores precisarão lidar o tempo todo com inventores e líderes criativos entre os alunos. Isso poderá ser visto por meio do crescimento constante do ensino de programação para crianças e atividades como a robótica, que vêm crescendo nas escolas dia após dia. <br><br>

             Do ponto de vista da gestão escolar, está se tornará cada vez mais digital e baseada na análise de grandes dados. E até por uma questão mercadológica, os gestores se verão forçados a ampliar conhecimentos s inovação e segurança. <br>

             Os professores e funcionários, por sua vez, deverão ter noção dos processos relacionados à IoT e como as tecnologias complementam sua rotina de trabalho. Com isso, naturalmente os colaboradores deverão ser capacitados para gerir suas próprias atividades e a liderança das organizações tenderá a ser muito mais horizontalizada. <br>

             Por fim, a relação com os pais também se modifica e a comunicação com a família deverá ser mais estreita, imediata e em busca de propósitos comuns, com a colaboração da tecnologia para a tomada de decisões.<br> 

             Os aplicativos serão uma importante ferramenta de comunicação, e tecnologias como os chatbots estarão cada vez mais presentes para agilizar o atendimento e responder a dúvidas e questionamentos. <br><br>
 
              Além disso, o acesso à informação s a vida escolar dos filhos será instantâneo e os feedbacks dependerão cada vez menos das tradicionais reuniões que acontecem, em geral, a cada bimestre. Com isso, o comportamento e o desempenho nas disciplinas, bem como o cumprimento das tarefas, estarão na palma da mão de pais e professores. <br>

              Vale dizer que se engana quem acha que as relações humanas estarão dissipadas. Pelo contrário, uma das premissas da transformação digital é que o ser humano está no centro de tudo. O que muda são as formas de interação, agora mais consistentes com informações qualificadas e uma gestão voltada ao uso das tecnologias. <br>
